package com.victorsashnev.tema13.Ejer07;

public class Limits {
    private final int MAX;
    private final int MIN;

    public Limits(int MAX, int MIN) {
        this.MAX = MAX;
        this.MIN = MIN;
    }

    public int getMAX() {
        return MAX;
    }

    public int getMIN() {
        return MIN;
    }
}
